package assign.assign6.com;

public class Calculatot {
	private int x;
	private int y;
	
	private int result;

	public Calculatot(int x, int y, int result) {
		super();
		this.x = x;
		this.y = y;
		this.result = result;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	public int getResult() {
		return result;
	}

	public void setResult(int result) {
		this.result = result;
	}
	
	

}
